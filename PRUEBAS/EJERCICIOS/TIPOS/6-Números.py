import math

print(abs(-5))  # DEVUELVE NÚMERO ABSOLUTO
print(abs(5))

######### DIVISION ###########
print(10 // 3)  # RESULTADO DE LA DIVISION.
print(10 % 3)  # RESTO DE LA DIVISION.

#####  REDONDEO ######
print(round(1.3))  # REDONDEO MATEMÁTICO (1)
print(round(1.5))  # (2)
print(round(1.7))  # (2)

print(math.ceil(1.1))  # REDONDEO A LA MAXIMA.
print(math.floor(1.999999))  # REDONDEO A LA MINIMA.


print(math.isnan(23))

print(math.pow(10, 3))  # POTENCIA DE UN NUMERO X ELEVADO A Y
print(math.sqrt(9))  # RAIZ CUADRADA DE X

print(math.gdc(40, 30, 50))
